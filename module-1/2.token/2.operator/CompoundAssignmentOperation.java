class CompoundAssignmentOperation {
	public static void main(String[] args){
		int i = 10;
		System.out.println("Content of i is : "+i);

		i += 25; // i=i+25
		System.out.println("Content of i is : "+i);

		i -= 25; // i=i-25
		System.out.println("Content of i is : "+i);

		i *= 25; // i=i*25
		System.out.println("Content of i is : "+i);

		i /= 25; // i=i/25
		System.out.println("Content of i is : "+i);

		i %= 25; // i=i%25
		System.out.println("Content of i is : "+i);



	}

}