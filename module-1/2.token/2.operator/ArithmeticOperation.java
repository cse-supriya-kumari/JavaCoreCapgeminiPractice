class ArithmeticOperation {
	public static void main(String[] args) {
		int i = 10;
		System.out.println("Content of  i  is :"+i);

		int j = 5;
		System.out.println("Content of j is :"+j);

		int a = i + j;
		System.out.println("Addation of " +i+ " and " +j+ " is :" +a);

		int b = i - j;
		System.out.println("Subtraction of " +i+ " and " +j+ " is :" +b);

		int c = i * j;
		System.out.println("Mutliplication of " +i+ " and " +j+ " is :" +c);

		int d = i / j;
		System.out.println("Division of " +i+ " and " +j+ " is :" +d);

		int e = i % j;
		System.out.println("Modulus of " +i+ " and " +j+ " is :" +e);

	}
}
