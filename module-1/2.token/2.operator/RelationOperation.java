class RelationOperation {
	public static void main(String[] args) {
		int i = 10;
		System.out.println("Conten of i is : "+i);

		int j = 50;
		System.out.println("Content of j is : "+j);

		boolean c = i > j;
		System.out.println("Is "+i+" > "+j+" : "+c);

		boolean d = i < j;
		System.out.println("Is "+i+" < "+j+" : "+d);

		boolean e = i != j;
		System.out.println("Is "+i+" != "+j+" : "+e);

		boolean f = i == j;
		System.out.println("Is "+i+" > "+j+" : "+f);
	}

}