class ArithmeticOperation {
	public static void addition(int i, int j) {
		System.out.println("Content of i is : "+i);
		System.out.println("Content of j is : "+j);
		int a = i + j;
		System.out.println("addation of "+i+" and "+j+" is "+a);
		System.out.println();
	}
	public static void substraction(int i, int j) {
		System.out.println("Content of i is : "+i);
		System.out.println("Content of j is : "+j);
		int a = i - j;
		System.out.println("substraction of "+i+" and "+j+" is "+a);
		System.out.println();
	}
	public static void multiplication(int i, int j) {
		System.out.println("Content of i is : "+i);
		System.out.println("Content of j is : "+j);
		int a = i * j;
		System.out.println("multiplication of "+i+" and "+j+" is "+a);
		System.out.println();
	}
	public static void division(int i, int j) {
		System.out.println("Content of i is : "+i);
		System.out.println("Content of j is : "+j);
		int a = i / j;
		System.out.println("division of "+i+" and "+j+" is "+a);
		System.out.println();
	}
	public static void modulus(int i, int j) {
		System.out.println("Content of i is : "+i);
		System.out.println("Content of j is : "+j);
		int a = i % j;
		System.out.println("modulus of "+i+" and "+j+" is "+a);
		System.out.println();
	}


}